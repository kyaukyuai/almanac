const FETCH_DOCS_IMPL = `// AUTO-GENERATED by almanac Stage 7. fetch_official_docs.
//
// input:  { url: string }
// output: { url, status, contentType, body, fetchedAt }

export async function fetchOfficialDocs(input: any, ctx: any): Promise<any> {
  if (!ctx || typeof ctx.fetch !== "function") {
    return {
      ok: false,
      error: {
        code: "network-not-allowed",
        message: "fetch_official_docs requires network capability; check the manifest's capabilities.network allowlist.",
        retryable: false,
      },
    };
  }
  const url = typeof input?.url === "string" ? input.url : "";
  if (url.length === 0) {
    return { ok: false, error: { code: "bad-input", message: "input.url is required", retryable: false } };
  }

  let parsed: URL;
  try {
    parsed = new URL(url);
  } catch {
    return { ok: false, error: { code: "bad-input", message: \`invalid url: "\${url}"\`, retryable: false } };
  }

  const fetchedAt = new Date().toISOString();
  let res: Response;
  try {
    res = await ctx.fetch(url, { redirect: "follow" });
  } catch (e: any) {
    const isAllowlist = e && e.name === "NetworkNotAllowedError";
    return {
      ok: false,
      error: {
        code: isAllowlist ? "network-not-allowed" : "fetch-failed",
        message: (e && e.message ? String(e.message) : "fetch failed").slice(0, 500),
        retryable: !isAllowlist,
      },
    };
  }

  const status = res.status;
  if (status >= 400) {
    return {
      ok: false,
      error: {
        code: status >= 500 ? "server-error" : "client-error",
        message: \`HTTP \${status} from \${url}\`,
        retryable: status >= 500 || status === 429,
      },
    };
  }

  const contentType = res.headers.get("content-type") ?? "application/octet-stream";
  const body = await res.text();
  const truncated = body.length > 200_000 ? body.slice(0, 200_000) : body;

  return {
    ok: true,
    data: {
      url,
      status,
      contentType,
      body: truncated,
      truncated: body.length > truncated.length,
      fetchedAt,
    },
    citations: [{ sourceId: hostnameToSourceId(parsed.hostname), url, fetchedAt }],
    freshness: { class: "slow", maxAge: 2592000, staleness: "fresh" },
  };
}

function hostnameToSourceId(host: string): string {
  return ("src-" + host.replace(/[^a-z0-9-]+/gi, "-")).toLowerCase().slice(0, 64);
}

export default fetchOfficialDocs;
`;

