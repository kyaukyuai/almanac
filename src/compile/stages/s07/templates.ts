const WEB_SEARCH_IMPL = `// AUTO-GENERATED by almanac Stage 7. web_search_recent.
//
// Recent-bias web search using DuckDuckGo's HTML endpoint (no API key).
// input:  { q: string, limit?: number }
// output: { results: Array<{ title, url, snippet }>, query, fetchedAt }

export async function webSearchRecent(input: any, ctx: any): Promise<any> {
  if (!ctx || typeof ctx.fetch !== "function") {
    return {
      ok: false,
      error: {
        code: "network-not-allowed",
        message: "web_search_recent requires network capability for duckduckgo.com.",
        retryable: false,
      },
    };
  }
  const q = typeof input?.q === "string" ? input.q.trim() : "";
  if (q.length === 0) {
    return { ok: false, error: { code: "bad-input", message: "input.q must be a non-empty string", retryable: false } };
  }
  const limit = Math.max(1, Math.min(20, Number.isFinite(input?.limit) ? Number(input.limit) : 5));

  const fetchedAt = new Date().toISOString();
  const ddgUrl = \`https://html.duckduckgo.com/html/?q=\${encodeURIComponent(q)}\`;

  let res: Response;
  try {
    res = await ctx.fetch(ddgUrl, {
      headers: { "user-agent": "Mozilla/5.0 (almanac/0.1) AppleWebKit/537.36" },
    });
  } catch (e: any) {
    return {
      ok: false,
      error: {
        code: e?.name === "NetworkNotAllowedError" ? "network-not-allowed" : "fetch-failed",
        message: String(e?.message ?? "fetch failed").slice(0, 500),
        retryable: e?.name !== "NetworkNotAllowedError",
      },
    };
  }
  if (res.status >= 400) {
    return {
      ok: false,
      error: {
        code: res.status >= 500 ? "server-error" : "client-error",
        message: \`DuckDuckGo returned HTTP \${res.status}\`,
        retryable: res.status >= 500 || res.status === 429,
      },
    };
  }
  const html = await res.text();
  const results = parseDuckDuckGoResults(html, limit);

  if (results.length === 0) {
    return {
      ok: false,
      error: { code: "no-results", message: \`no web results for "\${q}"\`, retryable: true },
    };
  }

  return {
    ok: true,
    data: { query: q, fetchedAt, results },
    citations: [{ sourceId: "src-duckduckgo", url: ddgUrl, fetchedAt }],
    freshness: { class: "fast", maxAge: 86400, staleness: "fresh" },
  };
}

function parseDuckDuckGoResults(html: string, limit: number): Array<{ title: string; url: string; snippet: string }> {
  const out: Array<{ title: string; url: string; snippet: string }> = [];
  // DDG HTML uses .result__a for links and .result__snippet for snippets.
  const blockRe = /<a[^>]+class=\\"result__a\\"[^>]+href=\\"([^\\"]+)\\"[^>]*>([\\s\\S]*?)<\\/a>[\\s\\S]*?<a[^>]+class=\\"result__snippet\\"[^>]*>([\\s\\S]*?)<\\/a>/g;
  let m: RegExpExecArray | null;
  while ((m = blockRe.exec(html)) !== null && out.length < limit) {
    const rawUrl = decodeDdgUrl(m[1] ?? "");
    if (!rawUrl) continue;
    out.push({
      url: rawUrl,
      title: stripHtml(m[2] ?? "").slice(0, 200),
      snippet: stripHtml(m[3] ?? "").slice(0, 500),
    });
  }
  return out;
}

function decodeDdgUrl(u: string): string {
  // DDG wraps result links as "/l/?uddg=<encoded-url>".
  try {
    if (u.startsWith("//")) u = "https:" + u;
    if (u.startsWith("/")) u = "https://duckduckgo.com" + u;
    const url = new URL(u);
    const ud = url.searchParams.get("uddg");
    if (ud) return decodeURIComponent(ud);
    return url.toString();
  } catch {
    return "";
  }
}

function stripHtml(s: string): string {
  return s.replace(/<[^>]+>/g, "").replace(/&nbsp;/g, " ").replace(/&amp;/g, "&").trim();
}

export default webSearchRecent;
`;

