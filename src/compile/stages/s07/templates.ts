const QUERY_FACTS_IMPL = `// AUTO-GENERATED by almanac Stage 7 (TemplateImplementer). Do not edit by hand.
//
// query_facts — FTS5 lookup against the indexed fact store.
//
// input:  { q: string, limit?: number, freshnessClass?: "static" | "slow" }
// output: { hits: Array<{ id, text, type, source, freshnessClass, validUntil }> }

export async function queryFacts(input: any, ctx: any): Promise<any> {
  if (!ctx || !ctx.knowledge || typeof ctx.knowledge.searchFacts !== "function") {
    return {
      ok: false,
      error: {
        code: "knowledge-missing",
        message: "query_facts requires the knowledge index; call \\\`almanac update\\\` to rebuild.",
        retryable: false,
      },
    };
  }

  const q = typeof input?.q === "string" ? input.q.trim() : "";
  if (q.length === 0) {
    return {
      ok: false,
      error: { code: "bad-input", message: "input.q must be a non-empty string", retryable: false },
    };
  }

  const limit = Math.max(1, Math.min(50, Number.isFinite(input?.limit) ? Number(input.limit) : 10));
  const opts: any = { limit, notExpiredAt: new Date().toISOString() };
  if (input?.freshnessClass === "static" || input?.freshnessClass === "slow") {
    opts.freshnessClass = input.freshnessClass;
  }

  let hits = await ctx.knowledge.searchFacts(q, opts);
  if (hits.length === 0) {
    for (const fallbackQ of fallbackQueries(q)) {
      hits = await ctx.knowledge.searchFacts(fallbackQ, opts);
      if (hits.length > 0) break;
    }
  }

  if (hits.length === 0) {
    return {
      ok: false,
      error: { code: "no-results", message: \`no facts matched "\${q}"\`, retryable: false },
    };
  }

  const nowIso = new Date().toISOString();
  const citations: any[] = [];
  const seenSources = new Set<string>();
  for (const h of hits) {
    if (seenSources.has(h.source.sourceId)) continue;
    seenSources.add(h.source.sourceId);
    citations.push({
      sourceId: h.source.sourceId,
      url: h.source.url,
      fetchedAt: h.extractedAt ?? nowIso,
      excerpt: h.source.excerpt,
    });
    if (citations.length >= 10) break;
  }

  // Determine the most volatile class among the hits to drive freshness.
  let anySlow = false;
  for (const h of hits) {
    if (h.freshnessClass === "slow") {
      anySlow = true;
      break;
    }
  }

  return {
    ok: true,
    data: {
      hits: hits.map((h: any) => ({
        id: h.id,
        text: h.text,
        type: h.type,
        source: { sourceId: h.source.sourceId, url: h.source.url },
        freshnessClass: h.freshnessClass,
        validUntil: h.validUntil,
        confidence: h.confidence,
      })),
    },
    citations,
    freshness: anySlow
      ? { class: "slow", maxAge: 2592000, staleness: "fresh" }
      : { class: "static", maxAge: null, staleness: "fresh" },
  };
}

function fallbackQueries(q: string): string[] {
  const seen = new Set<string>();
  const tokens: string[] = [];
  for (const token of q.match(/[A-Za-z0-9_']+/g) ?? []) {
    const key = token.toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    tokens.push(token);
  }
  if (tokens.length <= 1) return [];

  const out: string[] = [];
  const original = q.trim().toLowerCase();
  for (let size = Math.min(3, tokens.length - 1); size >= 1; size -= 1) {
    for (let start = 0; start + size <= tokens.length; start += 1) {
      const candidate = tokens.slice(start, start + size).join(" ");
      if (candidate.toLowerCase() === original) continue;
      out.push(candidate);
      if (out.length >= 8) return out;
    }
  }
  return out;
}

export default queryFacts;
`;

