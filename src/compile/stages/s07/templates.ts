/**
 * Canonical templates for the four default tools.
 *
 * Per `docs/design.md` §5, every almanac ships with the same four tools:
 *
 *   - query_facts         — FTS5 lookup against the indexed fact store
 *   - fetch_official_docs — capability-gated GET to a manifest-configured URL
 *   - web_search_recent   — recent-bias web search (DuckDuckGo HTML)
 *   - latest_releases     — GitHub releases for a manifest-configured repo
 *
 * Each entry below is a pair of *string sources*:
 *   - `implCode`  → written to `<almanacDir>/tools/<name>.ts`
 *   - `testCode`  → written to `<almanacDir>/tools/<name>.test.ts`
 *
 * The implementations are intentionally self-contained: they avoid imports
 * from the almanac source tree (which doesn't exist on disk after compile)
 * and only rely on globals (`fetch`, `URL`) plus the runtime-provided
 * `ctx.knowledge` / `ctx.fetch`. Bun loads them via the runtime's
 * `loadTool()` dynamic import.
 *
 * The smoke tests run with `bun test`, mock any network/knowledge access,
 * and assert envelope shape + key invariants.
 */

